export type OrderType = 'DINE_IN' | 'DELIVERY' | 'PICK_UP';
export type OrderStatus = 'PLACED' | 'PREPARING' | 'READY' | 'COMPLETED' | 'CANCELLED';
export type PaymentMode = 'CASH' | 'CARD' | 'UPI' | 'DUE' | 'PART';
export type TableStatus = 'AVAILABLE' | 'OCCUPIED' | 'RESERVED';

export interface Table {
  id: string;
  name: string;
  status: TableStatus;
  currentOrderId?: string;
  capacity?: number;
}

export interface RestaurantInfo {
  name: string;
  phone: string;
  address: string;
}

export type VegType = 'VEG' | 'NON_VEG' | 'BOTH';

export interface MenuItem {
  id: string;
  name: string;
  price: number;
  categoryId: string;
  isVeg: boolean;
  vegType: VegType;
  vegPrice?: number;
  nonVegPrice?: number;
  image?: string;
}

export interface Category {
  id: string;
  name: string;
}

export interface Addon {
  id: string;
  name: string;
  price: number;
  categoryId: string; // Which category this addon applies to
}

export interface SelectedAddon {
  id: string;
  name: string;
  price: number;
}

export interface CartItem extends MenuItem {
  quantity: number;
  selectedVegChoice?: 'VEG' | 'NON_VEG';
  selectedAddons?: SelectedAddon[];
}

export interface Order {
  id: string;
  billNo: string;
  customerName?: string;
  tableId?: string;
  tableName?: string;
  date: string;
  time: string;
  items: CartItem[];
  subtotal: number;
  tax: number;
  total: number;
  paymentMode: PaymentMode;
  orderType: OrderType;
  staffName: string;
  status: OrderStatus;
}

export interface SalesReportEntry {
  date: string;
  totalOrders: number;
  totalSales: number;
  paymentModes: Record<PaymentMode, number>;
}